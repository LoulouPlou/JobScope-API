# HELPER FUNCTIONS

def get_buzzwords_by_type(buzzword_type):
    """Get all buzzwords of a specific type."""
    return [bw for bw in BUZZWORDS_TECH if bw["type"] == buzzword_type]

def get_buzzwords_by_category(category):
    """Get all buzzwords of a specific category."""
    return [bw for bw in BUZZWORDS_TECH if bw["category"] == category]

def get_unique_types():
    """Get all unique types."""
    return sorted(set(bw["type"] for bw in BUZZWORDS_TECH))

def get_unique_categories():
    """Get all unique categories."""
    return sorted(set(bw["category"] for bw in BUZZWORDS_TECH))